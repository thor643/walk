function crearRutaPrueba(grupoPuntos){
	ruta = new Array();
	pathRuta = new Array();
	hora = 0;
	minuto = 0;
	fecha = new Date();
	identificador = fecha.getTime();

	for (var i = 0; i < grupoPuntos.length; i++) {
		if (hora < 10) {
			horas = "0"+hora;
		}
		if (minuto < 10) {
			minutos = "0"+minuto;
		}
		tiempo = horas+":"+minutos;
		latlon = new google.maps.LatLng(grupoPuntos[i].lat, grupoPuntos[i].lon);
		punto = new PuntoPrueba(grupoPuntos[i].lat, grupoPuntos[i].lon, 10, tiempo, ""+identificador+""+i);
		ruta.push(punto);
		pathRuta.push(latlon);
		minuto = minuto + 5;
		if (minuto == 60) {
			minuto = 0;
			hora = hora + 1;
		}
	}
	distanciaTotal = google.maps.geometry.spherical.computeLength(pathRuta);
	anadirRuta(ruta, distanciaTotal);
}

function PuntoPrueba(latitud, longitud, precision, hora, ident){
    this.latitud = latitud;
    this.longitud = longitud;
    this.precision = precision;
    this.hora = hora;
    uuid = device.uuid;
    this.identificador = uuid.concat(ident);

    this.distanciaConmigo = function(lati, longi){

        var punto1 = new google.maps.LatLng(this.latitud, this.longitud);
        var punto2 = new google.maps.LatLng(lati, longi);

        var distancia = google.maps.geometry.spherical.computeDistanceBetween(punto1, punto2);
        return distancia;
    }
}